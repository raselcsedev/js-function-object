var color = 'yellow';

switch(color){
    case 'blue':
        console.log('This color is blue');
        break;
    
    case 'white':
        console.log('This color is white');
        break;

    case 'red':
        console.log('This color is red');
        break;
        
    case 'yellow':
        console.log('This color is yellow');
        break;

    default : 
    console.log('This color will be Pink');
}